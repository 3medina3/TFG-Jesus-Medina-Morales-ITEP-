    private void CargarTablaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CargarTablaActionPerformed

        //Inicializamos el objeto cargar tablas 
        CargarTablas cargarTablas = new CargarTablas();
        
        int op = SelectDatos.getSelectedIndex();
        System.out.println(op);
        //Dependiendo de la opción que elijamos 
        switch (op) {
            case 0:
                
                //Cargamos los datos de las asignaturas 
                cargarTablas.cargarTablaAlumnosPorAsignatura(codigo, this,codProfesor);
                break;

            case 1:
                
                //Cargamos los datoss de las faltas 
                cargarTablas.cargarTablaAlumnosPorFaltas(codigo, this); 
                break;
        }


    }//GEN-LAST:event_CargarTablaActionPerformed

