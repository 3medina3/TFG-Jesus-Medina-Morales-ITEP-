    private void GraficarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GraficarActionPerformed

        //Damos paso a la ventana graficar a la que le mandaremos los datos 
        VentanaGraficarAlumno ventanaGraficar = new VentanaGraficarAlumno(this);

        //Mandamos los valores a la ventana graficar 
        DefaultCategoryDataset barChartDataset = new DefaultCategoryDataset();
        for (int i = 0; i < TablaVisible.getRowCount(); i++) {
            barChartDataset.setValue(Integer.parseInt(TablaVisible.getValueAt(i, 1).toString()), TablaVisible.getValueAt(i, 2).toString(), TablaVisible.getValueAt(i, 0).toString());
        }
        
        if (Radio3D.isSelected()) {

            //Generará la gráfica en 3D
            JFreeChart barChart = ChartFactory.createBarChart3D("Grafica de " + nombreAlumno.getText(), "Fechas", "Notas", barChartDataset, PlotOrientation.VERTICAL, true, true, false);
            CategoryPlot bPlot = barChart.getCategoryPlot();
            bPlot.setRangeGridlinePaint(Color.ORANGE);

            ChartPanel baPanel = new ChartPanel(barChart);
            baPanel.setBounds(500, 40, 500, 400);
            ventanaGraficar.getjPanel1().removeAll();
            ventanaGraficar.getjPanel1().add(baPanel, BorderLayout.CENTER);
            ventanaGraficar.getjPanel1().validate();
        } else {
            //Generará la gráfica en 2D
            JFreeChart barChart = ChartFactory.createBarChart("Grafica de " + nombreAlumno.getText(), "Fechas", "Notas", barChartDataset, PlotOrientation.VERTICAL, true, true, false);
            CategoryPlot bPlot = barChart.getCategoryPlot();
            bPlot.setRangeGridlinePaint(Color.ORANGE);

            ChartPanel baPanel = new ChartPanel(barChart);
            baPanel.setBounds(500, 40, 500, 400);
            ventanaGraficar.getjPanel1().removeAll();
            ventanaGraficar.getjPanel1().add(baPanel, BorderLayout.CENTER);
            ventanaGraficar.getjPanel1().validate();
        }

        this.setVisible(false);

    }//GEN-LAST:event_GraficarActionPerformed

