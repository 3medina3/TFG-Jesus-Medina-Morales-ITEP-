    private void jButton_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_EliminarActionPerformed

        //Inicializamos el objeto y pedimos confirmación
        buscar = new BusquedasMongo();
        int advertencia = JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar la falta con la id " + EliminarId.getText() + "?");

        //Si quiere eliminar
        if (advertencia == JOptionPane.YES_OPTION) {
            //Si no hay id 
            if (EliminarId.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "No ha insertado la id, por favor vuelva a intentarlo.");
            } else {
                //Guardamos el cursor 
                cursor = buscar.buscarFaltasProfesorPorCodigoEIdProfe(Integer.parseInt(EliminarId.getText()), Integer.parseInt(ventana.getjLabel_codigo().getText()));
                if (cursor.size() == 1) {

                    //Si encunetra la falta la borra
                    connectionMongoDB = new ConnectionMongoDB();
                    DBObject finddoc = new BasicDBObject("_id", Integer.parseInt(EliminarId.getText()));

                    connectionMongoDB.getColleccion().remove(finddoc);
                    DBCollection nuevaCollection = connectionMongoDB.getDb().getCollection("FaltasProfesor");
                    nuevaCollection.remove(finddoc);
                    JOptionPane.showMessageDialog(rootPane, "Se borró satisfactoriamente.");
                    EliminarId.setText("");
                    //Cerramos la conexión
                    connectionMongoDB.getMongo().close();
                } else {
                    //Sino lo encunetra muestra un mensaje 
                    JOptionPane.showMessageDialog(rootPane, "No se encontro ninguna falta asociado a este profesor.");
                    EliminarId.setText("");
                }
                cursor.close();
                dispose();
            }

        }

    }//GEN-LAST:event_jButton_EliminarActionPerformed

