    private void jButton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_guardarActionPerformed

        //Inicializamos los objetos
        conexion = new ConnectionMongoDB();
        buscar = new BusquedasMongo();
        try {
            //Si la fecha que hemos insertado es menor que la que es de hoy muestra un mensaje
            if (fecha_actual.compareTo(jDateChooser_Dia.getCalendar()) == 1) {
                JOptionPane.showMessageDialog(rootPane, "La fecha que ha insertado no es apropiada, por favor ponga una más acorde.");
                jDateChooser_Dia.setCalendar(null);
            }
            //Si no ha insertado ninguna fecha
            if (jDateChooser_Dia.getCalendar() == null) {
                JOptionPane.showMessageDialog(rootPane, "No ha puesto ninguna fecha.");
            } else {

                //Sacamos la colecction
                DBCollection nuevaCollection = conexion.getDb().getCollection("FaltasProfesor");

                int numDocumentos = 1;

                //Guardamos en el cursor la busqueda 
                cursor = buscar.buscarFaltasProfesorPorCodigo(numDocumentos);

                //Damos formato a la fecha
                SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
                String date = format.format(jDateChooser_Dia.getDate());

                //Mientras encunetre alguna falta con la misma id
                while (cursor.size() == 1) {
                    numDocumentos = numDocumentos + 1;
                    cursor = buscar.buscarFaltasProfesorPorCodigo(numDocumentos);
                }

                //Añadimos la falta al array
                faltas.add(new FaltasProfesores(numDocumentos, date, Integer.parseInt(ventana.getjLabel_codigo().getText()), ventana.getNombreProfesor().getText()));

                //Insertamos el array en la collection 
                for (FaltasProfesores faltasT : faltas) {
                    nuevaCollection.insert(faltasT.toDBObjectsFaltasProfesores());
                }

                //Mostramos mensaje de confirmación y limpiamos la fecha
                JOptionPane.showMessageDialog(rootPane, "Se añadió satisfactoriamente.");
                jDateChooser_Dia.setCalendar(null);
                cursor.close();
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(rootPane, "Lo siento, no ha puesto la fecha.");

        } catch (IllegalStateException e) {
            JOptionPane.showMessageDialog(rootPane, "Se ha producido un error con la base de datos. Por favor, rellene todos los campos para continuar");

        }

        //Cerramos la conexión
        conexion.getMongo().close();


    }//GEN-LAST:event_jButton_guardarActionPerformed

