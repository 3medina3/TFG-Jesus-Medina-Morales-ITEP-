    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
       //Le inicializamos nuestras variables
        buscar = new BusquedasMongo();
        cargar = new CargarTablas();

        try {

            //Le damos formato a nuestra fecha
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
            String date = format.format(jDateChooser_Dia.getDate());
            //Asignamos nuestro cursor con la fecha
            cursor = buscar.buscarFaltasPorDia(date);
            System.out.println(cursor.size());

            try {
                //Si encuentra la fecha 
                if (cursor.size() != 0) {
                    try {
                        //Carga los datos en la tabla con el c贸digo del alumno
                        cargar.cargarTablaFaltasAlumnos(this, cod, cursor);
                        jDateChooser_Dia.setDate(null);

                    } catch (NullPointerException e) {

                    }
                }
                //En caso de no encontrar la fecha
                if (cursor.size() == 0) {
                    JOptionPane.showMessageDialog(this, "No se ha encontrado a encontrado ninguna falta el dia " + date);
                    jDateChooser_Dia.setDate(null);

                }

                //Cerramos el cursor 
            } finally {
                cursor.close();

            }

        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(rootPane, "Lo siento, no ha puesto la fecha.");
        }

    }//GEN-LAST:event_jButtonBuscarActionPerformed

