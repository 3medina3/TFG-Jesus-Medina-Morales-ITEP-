    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
       //Inicializamos nuestros objetos
        buscar = new BusquedasMongo();
        cargarTablas = new CargarTablas();
        //Si las busquedas estan en blanco salta un mensaje
        if (Busquedas.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No ha puesto ninguna asignatura.");
        } else {
            //Buscamos las asignaturas por nombre
            cursor = buscar.buscarTodasNotasNombre(Busquedas.getText(), cod);
            System.out.println(cursor.size());

            try {
                //Si lo encuentra carga las tablas y limpia las busquedas 
                if (cursor.size() != 0) {
                    try {
                        cargarTablas.cargarTablaAsignaturasPorNombre(this, cursor);
                        Busquedas.setText("");
                    } catch (NullPointerException e) {

                    }
                }
                //En el caso de que no lo encuentre saltará un mensaje
                if (cursor.size() == 0) {
                    JOptionPane.showMessageDialog(this, "No tiene notas en esa asignatura  " + Busquedas.getText());
                    Busquedas.setText("");

                }

            } finally {
                cursor.close();

            }

        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed

