    private void graficarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_graficarActionPerformed
        // Llamamos a la ventana de graficar
        Ventanas.VentanaGraficarAlumno ventanaGraficarAlumno = new VentanaGraficarAlumno(this);

        //Nos generamos un objeto de tipo DefaultCategoryDataset
        DefaultCategoryDataset barChartDataset = new DefaultCategoryDataset();
        //Recorremos la tabla y sacamos los valores de la misma para mandarlos a la gráfica 
        for (int i = 0; i < TablaVisible.getRowCount(); i++) {
            barChartDataset.setValue(Integer.parseInt(TablaVisible.getValueAt(i, 2).toString()), TablaVisible.getValueAt(i, 3).toString(), TablaVisible.getValueAt(i, 1).toString());
        }
        //Creamos la gráfica
        JFreeChart barChart = ChartFactory.createBarChart3D("Grafica de " + nombreAlumno, "Fechas", "Notas", barChartDataset, PlotOrientation.VERTICAL, true, true, false);
        CategoryPlot bPlot = barChart.getCategoryPlot();
        bPlot.setRangeGridlinePaint(Color.ORANGE);

        //Generamos un ChartPanel y le asignamos las dimensiones 
        ChartPanel baPanel = new ChartPanel(barChart);
        baPanel.setBounds(500, 40, 500, 400);
        
        //Borramos el contenido del panel y le añadimos el CharPanel
        ventanaGraficarAlumno.getjPanel1().removeAll();
        ventanaGraficarAlumno.getjPanel1().add(baPanel, BorderLayout.CENTER);
        ventanaGraficarAlumno.getjPanel1().validate();

        this.setVisible(false);
    }//GEN-LAST:event_graficarActionPerformed

    private void botonRutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRutarActionPerformed
        // Botón que nos permite buscar la ruta donde pondremos nuestro documento 
        JFileChooser dlg = new JFileChooser();
        int op = dlg.showSaveDialog(this);
        if (op == JFileChooser.APPROVE_OPTION) {
            File f = dlg.getSelectedFile();
            RutaCompleta.setText(f.toString());
        }
    }//GEN-LAST:event_botonRutarActionPerformed

    private void reporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reporteActionPerformed
       
        //Guardamos el valor de la ruta y nos generamos un documento
        String ruta = RutaCompleta.getText();
        Document doc = new Document();
        try {
            //Si tenemos ruta
            if (!ruta.equalsIgnoreCase("")) {
                //Creamos un FileOutputStream de tipo pdf y le asignamos el nombre que le hemos puesto y la ruta 
                FileOutputStream archivo = new FileOutputStream(ruta + ".pdf");

                //Escribimos en el pdf y abrimos el documento 
                PdfWriter pdf = PdfWriter.getInstance(doc, archivo);
                doc.open();

                //Le asignamos al documento una imagen 
                Image imagenes = Image.getInstance("src/Imagenes/notas.png");
                doc.add(imagenes);

                //Creamos un contenedor para el pdf 
                PdfContentByte cb = pdf.getDirectContent();

                //Le asignamos el tipo de fuente
                BaseFont bf = BaseFont.createFont(BaseFont.COURIER_BOLD, BaseFont.CP1252, BaseFont.NOT_EMBEDDED);

                //Abrimos la zona de editar texto
                cb.beginText();

                cb.setFontAndSize(bf, 30);
                cb.setTextMatrix(50, 450);
                cb.showText("Calificaciones de " + nombreAlumno);

                cb.setFontAndSize(bf, 14);
                int y = 400;
                //For que recorre la tabla y va colocando los datos que hemos sacado de la tabla
                for (int i = 0; i < TablaVisible.getRowCount(); i++) {
                    y = y - 20;
                    cb.setTextMatrix(50, y);
                    cb.showText("Asignatura: " + TablaVisible.getValueAt(i, 1).toString() + "|| Nota: " + TablaVisible.getValueAt(i, 2).toString() + "|| Fecha: " + TablaVisible.getValueAt(i, 3).toString());
                }

                //Cerramos la zona de texto
                cb.endText();

                //Cerramos el documento
                doc.close();
                JOptionPane.showMessageDialog(this, "Pdf creado");
            }else{
                JOptionPane.showMessageDialog(this, "No ha puesto ninguna ruta, por favor introduzcala.");
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(VentanasAsignaturas.class.getName()).log(Level.SEVERE, null, ex);
        } catch (BadElementException ex) {
            System.out.println("La ruta de la imagen esta mal.");
        } catch (IOException ex) {
            Logger.getLogger(VentanasAsignaturas.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            System.out.println("Hay problemas con el documento.");
        }
    }//GEN-LAST:event_reporteActionPerformed

