    private void ModificarContraseñaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarContraseñaActionPerformed
        try {
            //Inicializamos la variable busquedas y conexion con la base de datos
            buscar = new BusquedasMongo();
            connectionMongoDB = new ConnectionMongoDB();
            //Asignamos la coleccion en la que vamos a buscar
            DBCollection nuevaCollection = connectionMongoDB.getDb().getCollection("Profesor");
            //Generamos dos cursores para buscar en las  dos tablas a la vez
            DBCursor cursor = buscar.buscarUsuarioNombreTelefono(Nombre.getText(), Integer.parseInt(Telefono.getText()));
            DBCursor cursor2 = buscar.buscarProfesorNombreTelefono(Nombre.getText(), Integer.parseInt(Telefono.getText()));
            //Nos creamos un objeto que va a ser el telefono que buscamos
            DBObject find = new BasicDBObject("telefono", Integer.parseInt(Telefono.getText()));

            //Si las dos contraseñas coinciden entramos
            if (newContrasenia.getText().equals(contraseniaRepetir.getText())) {
                //Confirmación
                int advertencia = JOptionPane.showConfirmDialog(null, "¿Esta seguro de cambiar su contraseña?");
                if (advertencia == JOptionPane.YES_OPTION) {
                    //Si ha encontrado a un profesor con esa contraseña la modifica
                    if (cursor2.size() != 0 && cursor.size() == 0) {

                        update = new BasicDBObject();
                        update.append("contrasenia", newContrasenia.getText());
                        setObject = new BasicDBObject();
                        setObject.append("$set", update);
                        nuevaCollection.update(find, setObject);
                        JOptionPane.showMessageDialog(rootPane, "Contraseña del profesor: " + Nombre.getText() + " cambiada.");
                        dispose();
                        ventanaLogeo.setVisible(true);

                    }
                    //Si encuentra a n alumno con esa contraseña la modifica
                    if (cursor.size() != 0 && cursor2.size() == 0) {
                        update = new BasicDBObject();

                        update.append("contrasenia", newContrasenia.getText());

                        setObject = new BasicDBObject();
                        setObject.append("$set", update);

                        connectionMongoDB.getColleccion().update(find, setObject);
                        JOptionPane.showMessageDialog(rootPane, "Contraseña del alumno: " + Nombre.getText() + " cambiada.");
                        dispose();
                        ventanaLogeo.setVisible(true);
                    }
                    //Si no se ha encontrado a nigun usuario con esas credencales en la base de datos se muestra un mensaje.
                    if (cursor.size() == 0 && cursor2.size() == 0) {
                        JOptionPane.showMessageDialog(rootPane, "No se ha encontrado al usuario con ese numero de telefono, vuelva a intentarlo .");
                        Nombre.setText("");
                        Telefono.setText("");
                        contraseniaRepetir.setText("");
                        newContrasenia.setText("");
                    }

                }
                //En el caso de que no coincidan las contraseñas se limpian los campos
            } else {
                JOptionPane.showMessageDialog(this, "No coinciden las contraseñas");
                newContrasenia.setText("");
                contraseniaRepetir.setText("");
            }
        } catch (NumberFormatException e) {
            //Controla que no pueda meter letras en el telefono
            JOptionPane.showMessageDialog(this, "El telefono no puede ser una letra");
        }

    }//GEN-LAST:event_ModificarContraseñaActionPerformed

    private void TelefonoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TelefonoKeyTyped

        char validar = evt.getKeyChar();
        //Controla que no pueda meter letras en el telefono con el teclado numérico
        if (Character.isLetter(validar)) {
            getToolkit().beep();
            evt.consume();

            JOptionPane.showMessageDialog(rootPane, "Solo pueden insertarse numeros");
        }
    }//GEN-LAST:event_TelefonoKeyTyped

